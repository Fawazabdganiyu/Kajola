import { Schema, model } from 'mongoose';

const userSchema = new Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
  city: {
    type: String,
    required: true,
  },
  state: {
    type: String,
    required: true,
  },
  userType: {
    type: String,
    enum: ['Buyer', 'Seller'],
    default: 'Buyer',
  },
  desc: String,
  img: String,
}, {
  timestamps: true
});

export default model('User', userSchema);
